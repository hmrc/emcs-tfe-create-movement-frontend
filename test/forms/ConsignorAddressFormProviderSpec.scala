package forms

import forms.behaviours.StringFieldBehaviours
import play.api.data.FormError

class ConsignorAddressFormProviderSpec extends StringFieldBehaviours {

  val form = new ConsignorAddressFormProvider()()

  ".property" - {

    val lengthKey = "consignorAddress.property.error.length"
    val maxLength = 11

    val fieldName = "property"

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )
  }

  ".street" - {

    val requiredKey = "consignorAddress.street.error.required"
    val lengthKey = "consignorAddress.street.error.length"
    val maxLength = 65

    val fieldName = "street"

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )

    behave like mandatoryField(
      form,
      fieldName,
      requiredError = FormError(fieldName, requiredKey)
    )
  }

  ".town" - {

    val requiredKey = "consignorAddress.town.error.required"
    val lengthKey = "consignorAddress.town.error.length"
    val maxLength = 50

    val fieldName = "town"

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )

    behave like mandatoryField(
      form,
      fieldName,
      requiredError = FormError(fieldName, requiredKey)
    )
  }

  ".postcode" - {

    val requiredKey = "consignorAddress.postcode.error.required"
    val lengthKey = "consignorAddress.postcode.error.length"
    val maxLength = 10

    val fieldName = "postcode"

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )

    behave like mandatoryField(
      form,
      fieldName,
      requiredError = FormError(fieldName, requiredKey)
    )
  }
}
